// Modulo (%)
// Operador binario. Retorna o inteiro restante da divisao dos dois operados.

12 % 5 // retorna 2

// Incremento (++) e Decremento (--)
++X
x--

// Negação (-) e Adição (+)
-3
+"3" // retorna 3
+true // retorna 1
+false // retorna 0
