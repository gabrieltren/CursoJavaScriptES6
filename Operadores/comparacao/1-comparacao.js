// Igual (==)
// Retorna verdadeiro caso os operados sejam iguais. 3 == var1
"3" == var1
3 == "3"

// N達o igual (!=)
// Retorna verdadeiro caso os operados n達o sejam iguais. var1 != 4
var2 != 3

// Estritamente igual (===)
// Retorna verdadeiro caso os operados sejam iguais e do mesmo tipo. 
3 === var1

// Estritamente n達o igual (!==)
// Retorna verdadeiro caso os operados nao sejam iguais e/ou n達o sejam do mesmo tipo
var1 !== "3"
3 !== "3"

// Maior que (>)
// Retorna verdadeiro caso o operando da esquerda seja maior do que o da direita
var2 > var1
"12" > 2

// Maior que ou igual (>=)
// Retorna verdadeiro caso o operando da esquerda seja maior ou igual o da direita
var2 >= var1
var1 >= 3

// Menor que (<)
// Retorna verdadeiro caso o operando da esquerda seja menor  que o da direita
var1 < var2
3 < 8

// Menor que ou igual (<=)
// Retorna verdadeiro caso o operando da esquerda seja menor ou igual ao da direita
var1 <= var2
var1 <= 6
